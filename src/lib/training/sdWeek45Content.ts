/**
 * State Director — Weeks 4 & 5 full content (Staffing, Recruiting & Operations;
 * State Ownership & Leadership).
 *
 * Source of truth for Week 4 and Week 5 module content:
 *   - SD_W45_TRAINING_SPECS  → Training-level overrides keyed by display title.
 *   - SD_W45_FULL_CONTENT    → Curated SDFullContent payloads keyed by
 *                              generated module id (sd-w{week}d{day}-{slug}).
 *
 * Goal: a new State Director can complete every Week 4 & Week 5 module —
 * including shadowing and final readiness — without needing a mentor to fill
 * in operational gaps. Shadowing modules carry observation goals, pre-shadow
 * questions, post-shadow reflection, evidence, and mentor sign-off copy.
 * Final readiness modules include readiness checklist, sign-off, and
 * certification language.
 */
import type { SDFullContent, SDWalkStep, SDKnowledgeQ } from "./stateDirectorFullTraining";
import type { SDTrainingSpecOverride } from "./sdWeek1Content";

function step(
  action: string,
  lookFor: string,
  owner: string,
  escalation?: string,
): SDWalkStep {
  return escalation ? { action, lookFor, owner, escalation } : { action, lookFor, owner };
}

function quiz(
  idBase: string,
  items: Array<{ q: string; opts: string[]; answer: number }>,
): SDKnowledgeQ[] {
  return items.map((it, i) => ({
    id: `${idBase}-q${i + 1}`,
    question: it.q,
    options: it.opts,
    answerIndex: it.answer,
  }));
}

